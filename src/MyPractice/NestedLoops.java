package MyPractice;

public class NestedLoops {
    public static void main(String[] args) {
        for ( int a=1; a<=6; a++){
            for (int b=1; b<=6; b++){
                for ( int c=1; c<=6; c++){
                    System.out.print(a);
                    System.out.print(b);
                    System.out.print(c);
                    System.out.println();
                }

            }
        }

    }



}
