package day16_ForLoop;

public class Break_Statement {

    public static void main(String[] args) {

        for (int i= 1; i<=5; i++){
            System.out.println("Hello world");
            break;
        }

        for (char i = 'a'; i<= 'z'; i++){
            System.out.println(i);

            if (i =='d'){
                break;
            }
        }









    }
}
